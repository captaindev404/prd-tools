/**
 * Main monitoring loop
 */
async function monitor() {
  await initialize();

  console.log('üöÄ Starting progress monitor...\n');

  // Initial display
  await displayDashboard();

  // Refresh every 5 seconds
  setInterval(async () => {
    try {
      await displayDashboard();
    } catch (err) {
      console.error('Error updating dashboard:', err);
    }
  }, REFRESH_INTERVAL);

  // Handle graceful shutdown
  process.on('SIGINT', async () => {
    console.log('\n\nüëã Shutting down monitor...');
    await redis.quit();
    process.exit(0);
  });
}

// Run monitor
if (require.main === module) {
  monitor().catch((err) => {
    console.error('‚ùå Monitor failed:', err);
    process.exit(1);
  });
}

export { monitor, displayDashboard };
